public class loops {
    public static void main(String a[]){
        // While loop
        int i =0;
        while(i<4){
            System.out.println(i);
            i++;
        }
        // do while  =>>> run aleast one time 
        int j = 5;
        do{
             System.out.println(j);
            j++;
        }while(j<4);

        // for loop
        for(i=0;i<4;i++){
            System.out.println(i);
        }

        
    }
}
